model.compile(optimizer='SGD', loss='categorical_crossentropy', metrics=['accuracy'])
model.fit(X_train, Y_train, batch_size=BATCH_SIZE, epochs=EPOCHS,
		  verbose=VERBOSE, validation_split=VALIDATION_SPLIT)

